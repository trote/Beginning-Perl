#!/usr/bin/perl -w
# expensive_calculator.pl --- Expensive Calculator

use warnings;
use strict;

print "7 + 3 = ", 7 + 3, "\n";
print "7 - 3 = ", 7 - 3, "\n";
print "7 * 3 = ", 7 * 3, "\n";

print "7 / 3 = ", 7 / 3, "\n";
print "7.0 / 3.0 = ", 7.0 / 3.0, "\n";

print "7 % 3 = ", 7 % 3, "\n";

print "7 + 3 * 5 = ", 7 + 3 * 5, "\n";
print "(7 + 3) * 5 = ", (7 + 3) * 5, "\n";

__END__

